import math

def add(num1, num2):
    return num1 + num2

def subtract(num1, num2):
    return num1 - num2

def divide(num1, num2):
    return num1 / num2

def multiply(num1, num2):
    return num1 * num2

def areaofcircle(radius):
    return round( math.pi * (radius*radius), 2)

def areaofsquare(length, width):
    return length * width

